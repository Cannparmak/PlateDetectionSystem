document.addEventListener("alpine:init", () => {
  const normalizeLang = (lang) => {
    if (!lang) return "tr";
    const value = String(lang).toLowerCase();
    if (value.startsWith("en")) return "en";
    return "tr";
  };

  const getCookie = (name) => {
    const value = `; ${document.cookie}`;
    const parts = value.split(`; ${name}=`);
    if (parts.length === 2) {
      return parts.pop().split(";").shift();
    }
    return null;
  };

  const setCookie = (name, value) => {
    document.cookie = `${name}=${value}; path=/; max-age=31536000; samesite=lax`;
  };

  const initialLang = normalizeLang(
    getCookie(LANG_COOKIE) ||
    localStorage.getItem("lang") ||
    document.documentElement.lang ||
    navigator.language
  );

  Alpine.store("app", {
    // ── Theme ─────────────────────────────────────────────────────
    theme: localStorage.getItem("theme") || "dark",

    get isDark() { return this.theme === "dark"; },

    toggleTheme() {
      this.theme = this.isDark ? "light" : "dark";
      localStorage.setItem("theme", this.theme);
      this._applyTheme();
    },

    _applyTheme() {
      if (this.isDark) {
        document.documentElement.classList.add("dark");
        document.documentElement.classList.remove("light");
        document.documentElement.style.colorScheme = "dark";
      } else {
        document.documentElement.classList.remove("dark");
        document.documentElement.classList.add("light");
        document.documentElement.style.colorScheme = "light";
      }
    },

    // ── Language ──────────────────────────────────────────────────
    lang: initialLang,

    get isEN() { return this.lang === "en"; },

    toggleLang() {
      this.lang = this.isEN ? "tr" : "en";
      localStorage.setItem("lang", this.lang);
      setCookie(LANG_COOKIE, this.lang);
      this._applyLanguage();
    },

    _applyLanguage() {
      document.documentElement.lang = this.lang;
    },

    t(key) {
      return TRANSLATIONS[this.lang]?.[key] ?? TRANSLATIONS["tr"]?.[key] ?? key;
    },

    planName(name) {
      return PLAN_NAME_MAP[this.lang]?.[name] ?? name;
    },

    // ── Init ──────────────────────────────────────────────────────
    init() {
      this._applyTheme();
      this._applyLanguage();
      localStorage.setItem("lang", this.lang);
      setCookie(LANG_COOKIE, this.lang);
    }
  });
});
